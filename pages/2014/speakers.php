<article id="ic">
    <h3>Benoit Savoret</h3>
    <h3>Former COO EMEA, Lehman Brothers | Former Head of Global Equities, Nomura</h3>
    <br /><center><img src="./img_2013/speakers/benoit.jpg" width = "200"/></center>
<br />
<p align="justify">
Benoit Savoret graduated in engineering at the Ecole Centrale de Paris with a specialization in economy and finance at the Institute of Politic Sciences. After his military service for the Navy in the French Nuclear Agency, where he did research on optimization of electricity production, he trained at Rothchild and CIE Bank. He then spent seven years in the derivative department of Societe Generale, as a trader, where he was posted in Philadelphia, London, Tokyo and Hong Kong. 
</p><p align="justify">
In 1997, Mr Savoret joined Lehman Brothers derivatives team in London. By 2000 he was global head of derivatives trading. He then became a Managing Director and CEO of Asia Equities and later head of European Equities. His last position was COO for Europe and the Middle-East, member of the firm Executive Committee. In 2011 Mr Savoret joined Nomura as co-Global Head of Equities, a Member of the Wholesales Executive Committee and Executive Management board to finally become an Advisor to the joint heads of Capital Markets. Currently, Mr Savoret is on the boards Ecole Centrale Paris and Eptica a global provider of multichannel customer interaction software.
</p>

</article>

