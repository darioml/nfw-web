    <h3>Imperial College Finance Society</h3>
<p>
    The Imperial College Finance Society (ICFS) is the largest, most
    established society for university students seeking an early insight into
    finance and business as prospective career paths. Established in 1999,
    ICFS is experienced in providing innovative careers solutions to its
    members. We aim to foster a vibrant, interactive community for learning
    and networking with future employers/colleagues, peers/alumni and students
    at other top institutions globally.
</p>
<p>
    With over 2800 members across all departments of Imperial College and the
    Business School, we aim to provide our members with the necessary wealth
    of resources and opportunities needed to support them in achieving their
    potential in their professional pursuits. Our success as a society can
    really be seen through the success of our members, gaining spring week
    positions, summer internships and graduate jobs in highly competitive
    sectors.
</p>
<p>
    We hold interactive events with the markets leading investment banks and
    financial institutions - providing students with vital networking
    opportunities. By inviting leading professionals to give presentations on
    a wide range of topics, we can really help our members uncover their hidden
    strengths and develop their understanding of the global financial industry.
</p>
<p>
