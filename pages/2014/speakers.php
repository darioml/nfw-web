    <h3>Co-Head of Equity Capital Markets EMEA, J.P. Morgan</h3>
    <br /><center><img src="./img_2013/speakers/achintya.jpg" width = "200"/></center>
<p align="justify">
Achintya Mangla is a Managing Director and Co-Head of Equity Capital Markets for Europe, Middle East &amp; Africa at J.P.Morgan in London. Before moving to London in August 2013 in his current role, Achintya spent more than 11 years with J.P.Morgan in Hong Kong in various roles within Equity Capital Markets. Most recently, Achintya was Co-Head for Asia ex-Japan Equity Capital Markets.
</p>
