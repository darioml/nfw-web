<article id="ta">
    <h3>Arne Staal</h3>
    <h3>Head of Index Products &amp; Systematic Strategies Research Europe, Barclays</h3>
<br /><center><img src="./img_2013/speakers/arne.jpg" width = "200"/></center>
<p align="justify">
Arne Staal is a Director and European Head of Systematic Strategies & Index Products within the Index, Portfolio and Risk Solutions team at Barclays, based in London.  The group designs investment strategies and indices across all asset classes and publishes on all aspects of systematic investing.
</p><p align="justify">
Mr. Staal joined Barclays in 2008 from Lehman Brothers where he worked in the Portfolio Modelling team on risk measurement and portfolio construction.  
</p><p align="justify">
Mr. Staal has a PhD in Finance from the Kellogg School of Business at Northwestern University, and holds Master degrees in Economics and Econometrics & Operations Research from the University of Groningen in the Netherlands.
</p>
</article>

<article id="mp">
    <h3>Omiros Sarikas</h3>	
    <h3>M&amp;A Director, AHV Associates</h3>
<br /><center><img src="./img_2013/speakers/omiros.png" width = "150" /></center>
<p align="justify">
Omiros D. Sarikas is a Mergers and Acquisitions financier at AHV Associates LLP, an award winning London-based advisory firm. He joined AHV from Candesic, a McKinsey alumni strategy consultancy, where he was mainly involved in private equity transactions, such as Cinven’s acquisition of Sebia for €800 million.
</p><p align="justify">
Omiros has worked on projects in London, Zürich and New York for venture capital and private equity funds of various types and sizes (from $200 million to $20 billion of assets under management), including Welsh, Carson, Anderson &amp; Stowe, Ares Life Sciences (Ernesto Bertarelli’s fund), Cinven, ECI Partners, Silverfleet Capital and Hutton Collins.
</p><p align="justify">
In addition, he has advised on strategic M&amp;A business projects for IntraLinks, Deloitte Corporate Finance, AMEX (S&amp;P 500 serial acquirer with $147 billion assets), Credit Suisse (First Boston) Investment Bank and Lyceum Capital private equity fund.
</p><p align="justify">
At AHV, Omiros covers global M&amp;A and sponsored (i.e. private equity-backed) transactions. Following the company’s recent expansion, it increased its mean deal size exponentially and has been awarded the titles of ‘Corporate Finance Boutique of the Year’, ‘Cross Border M&amp;A Advisory of the Year’ and ‘Middle Market Investment Bank of the Year’ at a number of awards over the last four years including ACQ, DealMakers Monthly, IFR and FI Global.
</p><p align="justify">
Overall, Omiros has completed over 40 assignments to date in sectors such as TMT, Recycling, Energy, Financial Services, Industrials, Hospitality and Healthcare. This includes advisory work for private and public businesses, including BT, Vodafone, Samsung, Aspen Healthcare, Martindale Pharma, Picsel, EDF, E.ON, Saudi Telecom and Morgan Stanley.
</p>
</article>

<article id="os">
    <h3>Pasquale Della Corte</h3>	
    <h3>Assistant Professor of Finance, Imperial College Business School</h3>
<br /><center><img src="./img_2013/speakers/pasquale.jpg" width = "200" /></center>
<p align="justify">
Pasquale Della Corte is an Assistant Professor of Finance at Imperial College Business School, Imperial College London, and serves as an Associate Editor of the Journal of Money, Credit and Banking (JMCB). Prior to joining Imperial College, Pasquale was an Assistant Professor of Finance at Warwick Business School, University of Warwick. He has also held visiting research positions at the the Washington University in St. Louis, Federal Reserve Bank of St. Louis, Norges Bank, and the Hong Kong Institute for Monetary Research. Pasquale has received Awards for Teaching Excellence both at Warwick (2009) and Imperial College London (2013)
</p><p align="justify">
Pasquale’s research focuses on international finance, global asset allocation, empirical asset pricing, portfolio choice, market microstructure, and Bayesian econometrics. His research has been published in the Journal of Financial Economics, the Review of Economics and Statistics, the Review of Financial Studies, and the Journal of Empirical Finance. I have also received the INQUIRE UK 2010 and INQUIRE UK 2011 Best Paper Awards, and KEPOS CAPITAL Award for the Best Paper on Investments at the 2013 WFA meetings.
</p><p align="justify">
Pasquale holds a Laurea in Economics and Banking summa cum laude from the University of Siena (Italy), a Master in Economics and Finance from the University of Brescia (Italy), and a PhD in Finance from the University of Warwick.
</p>
