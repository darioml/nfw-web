		Who is organising the conference?
	</dt>
	<dd>
The New Financial Worlds Conference <a href="team">committee</a> is fully responsible for organising the New Financial Worlds Conference. The committee is entirely led by students at Imperial College and is part of the <a href="http://www.financesociety.co.uk">Imperial College Finance Society</a>.
	</dd>
	<dt>How can I keep informed about the conference?
	</dt>
	<dd>
The best way to get the latest updates regarding the conference would be by liking <a href="http://www.facebook.com/NFWConference">our Facebook Page</a> or visiting this website regularly.
	</dd>
	
	<dt>Who is eligible to apply?
	</dt>
	<dd>
Candidates from all backgrounds are eligible to apply! We encourage all students, both undergraduate and postgraduate, to apply.
	</dd>
	<dt>How do I register for the Conference?</dt>
	<dd>
		You can apply online, <a href="./register">here</a>. Please note that applicants will be required to submit their CV for further consideration.
	</dd>
<dt>When can I apply?</dt>
<dd>
The application process is now open, apply <a href="./register">here</a>.
</dd>
<dt>
When can I find out if I have been selected to attend the Conference?
</dt>
<dd>
This decision will be communicated to you a couple of days after the closing of registrations. For international applicants however, we will do our best to communicate our decision within 3 working days from your application, so that you can have plenty of time to plan your flight and accommodation arrangements.
</dd>
<dt>When am I required to pay?</dt>
<dd>
Once successful candidates have been selected to attend the Conference you will be contacted to confirm your place and pay.
</dd>
<dt>How much are the tickets?
</dt>
<dd>
Tickets are priced at &pound;30 (inc. VAT).
</dd>
<dt>What is included in the ticket price?</dt>
<dd>
Successful candidates will have access to all presentations given on the day along with panel discussions and workshop sessions. Furthermore, they will be provided with lunch and refreshments during all breaks.
</dd>
<dt>Are tickets refundable?</dt>
<dd>
Unfortunately tickets are non-refundable.
</dd>
<dt>
How will I have proof of purchase and ticket?
</dt>
